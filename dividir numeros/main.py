# Divide los numeros que le digas, opera con decimales


print("Recuerda no dividir entre cero")
x = float(input("Escribe un número: "))
y = float(input("Escribe el otro número: "))
if x == 0:
        print("El cero no vale, cualquier numero dividido por cero siempre va a ser cero")
elif x <= 0:
        print("Tu numero es bajo cero", x / y )
elif x > 0:
        print("El resultado es", x / y )



